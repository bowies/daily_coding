--Mysql
SELECT
    O.ANIMAL_ID,
    O.NAME
FROM
    ANIMAL_OUTS AS O
	LEFT JOIN
    ANIMAL_INS AS I
	ON
    O.ANIMAL_ID = I.ANIMAL_ID
WHERE
    I.ANIMAL_ID IS NULL;

--Oracle
SELECT
    O.ANIMAL_ID,
    O.NAME
FROM ANIMAL_INS I
         RIGHT JOIN
     ANIMAL_OUTS O
     ON I.ANIMAL_ID = O.ANIMAL_ID
WHERE
    I.ANIMAL_ID IS NULL
ORDER BY
    O.ANIMAL_ID;